const christmasTreeArray = [];
function createCristmasTree(height) {
    for (let i = 0; i < height; i++) {
        let replace = '';
        for (let x = 0; x < newArray[i].length; x++) {
            if (newArray[i][x] <= 5) {
                replace += ' ';
            } else {
                replace += '*';
            }
        }
        christmasTreeArray.push(replace);
    }
}
