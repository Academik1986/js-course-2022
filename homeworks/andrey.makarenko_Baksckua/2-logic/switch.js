const firstNumber = 8;
const secondNumber = 6;
const operation = '+';

if (operation === '+') {
    console.log(firstNumber + secondNumber);
} else if (operation === '-') {
    console.log(firstNumber - secondNumber);
} else if (operation === '*') {
    console.log(firstNumber * secondNumber);
} else if (operation === '/') {
    console.log(firstNumber / secondNumber);
}
