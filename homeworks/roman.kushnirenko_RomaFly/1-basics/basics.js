const numFirst = 15;
const numSecond = 7;

console.log('*** Арефметичні операції над двома числами: 15 і 7 ***');

console.log(`Додавання: ${numFirst + numSecond}`);
console.log(`Віднімання: ${numFirst - numSecond}`);
console.log(`Множення: ${numFirst * numSecond}`);
console.log(`Ділення: ${(numFirst / numSecond).toFixed(2)}`);
console.log(`Остача від ділення: ${numFirst % numSecond}`);
console.log(`Піднесення до степеня: ${numFirst ** numSecond}`);
